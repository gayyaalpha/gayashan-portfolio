# ── Evaluation ────────────────────────────────────────────────────────────────
def evaluate_pipeline():
    print(f"Running {len(TEST_SET)} questions through the LangGraph agent...\n")

    questions, answers, contexts_list, ground_truths = [], [], [], []

    for i, item in enumerate(TEST_SET):
        print(f"  [{i+1}/{len(TEST_SET)}] {item['question'][:65]}...")

        # Real agent answer + the actual chunks the agent retrieved during the run
        # (LLM-formulated query, not a separate re-retrieval against the raw question)
        answer, contexts = generate_ai_response(item["question"])

        print(f"\n  ── ANSWER ──────────────────────────────────────────────────────")
        print(f"  {answer}")
        print(f"\n  ── CONTEXTS ({len(contexts)} chunks) ───────────────────────────────")
        for j, chunk in enumerate(contexts):
            print(f"  [chunk {j+1}] {chunk}")
        print(f"  ────────────────────────────────────────────────────────────────\n")

        questions.append(item["question"])
        answers.append(answer)
        contexts_list.append(contexts)
        ground_truths.append(item["ground_truth"])

    print("\nScoring with RAGAS (judge: gpt-4o)...\n")

    dataset = Dataset.from_dict({
        "question":     questions,
        "answer":       answers,
        "contexts":     contexts_list,
        "ground_truth": ground_truths,
    })

    result = evaluate(
        dataset,
        metrics=metrics,
        llm=judge_llm,
        embeddings=judge_embeddings,
    )

    print("\n── Per-question RAGAS Scores ─────────────────────────────────────────")
    df = result.to_pandas()
    metric_cols = ["faithfulness", "answer_relevancy", "context_precision", "context_recall"]
    available = [m for m in metric_cols if m in df.columns]
    for i, row in df.iterrows():
        q = questions[i]
        a = answers[i]
        print(f"\n  Q{i+1}: {q}")
        print(f"  Answer:")
        for line in a.splitlines() or [a]:
            print(f"    {line}")
        print(f"  Scores:")
        for m in available:
            print(f"    {m:18}: {float(row[m]):.4f}")

    print("\n── Average RAGAS Scores ──────────────────────────────────────────────")
    print(result)
    print("─────────────────────────────────────────────────────────────────────")

    return result


if __name__ == "__main__":
    evaluate_pipeline()
