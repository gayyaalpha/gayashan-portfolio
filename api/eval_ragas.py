"""
eval_ragas.py
─────────────
RAGAS evaluation of the existing LangGraph agent pipeline.

- Answer  : generated by the real LangGraph agent (generate_ai_response)
- Contexts: the actual chunks the agent retrieved during the run (LLM-formulated
            tool query, captured from ToolMessage in the graph state)
- Judge   : gpt-4o (smarter than the pipeline model gpt-4o-mini)

Metrics:
    Faithfulness       — is the answer grounded in the retrieved chunks?
    Answer Relevancy   — does the answer actually address the question?
    Context Precision  — are the retrieved chunks relevant to the question?
    Context Recall     — did we retrieve all the context needed to answer?

Usage:
    python eval_ragas.py
"""

import os
from dotenv import load_dotenv

load_dotenv()

from langchain_openai import ChatOpenAI, OpenAIEmbeddings
from ragas import evaluate
from ragas.metrics import (
    faithfulness,
    context_precision,
    context_recall,
    AnswerRelevancy,
)
from ragas.llms import LangchainLLMWrapper
from ragas.embeddings import LangchainEmbeddingsWrapper
from datasets import Dataset

from ai_agent import generate_ai_response

# ── Judge model + embeddings (smarter than the pipeline model being evaluated) ─
# LangChain wrappers expose the .embed_query() / .generate() interface RAGAS expects.
judge_llm = LangchainLLMWrapper(
    ChatOpenAI(model="gpt-4o", api_key=os.getenv("OPENAI_API_KEY"))
)
judge_embeddings = LangchainEmbeddingsWrapper(
    OpenAIEmbeddings(model="text-embedding-3-small", api_key=os.getenv("OPENAI_API_KEY"))
)

# AnswerRelevancy needs 3 reverse-questions per answer (strictness=3) to produce a
# stable similarity score. ChatOpenAI ignores runtime n=, so n must be baked into
# the constructor — hence a dedicated LLM just for this metric.
answer_relevancy_llm = LangchainLLMWrapper(
    ChatOpenAI(model="gpt-4o", api_key=os.getenv("OPENAI_API_KEY"), n=3, temperature=0.7)
)
answer_relevancy = AnswerRelevancy(
    strictness=3,
    llm=answer_relevancy_llm,
    embeddings=judge_embeddings,
)

metrics = [faithfulness, answer_relevancy, context_precision, context_recall]


